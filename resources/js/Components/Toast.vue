const displayed = ref(false);

watchEffect(async () => {
    level.value = usePage().props.toast.level || '';
    message.value = usePage().props.toast.message || '';
    
    if (message.value && !displayed.value) {
        // Show the toast if there's a message and it's not already displayed
        show.value = true;
        displayed.value = true;

        // Set a timeout to close the toast after 5 seconds
        setTimeout(() => {
            close();
        }, 5000);
    }
   
});

const close = () => {
    show.value = false;
    displayed.value = false; // Reset displayed flag
    usePage().props.toast.level = null;
    usePage().props.toast.message = null;
};

